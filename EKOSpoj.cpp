#include <bits/stdc++.h>

using namespace std;
    // {}   []
long long int n, h;

long long int BinSearch(long long int min,long long int max,long long int treeH[]){
    long long int mid = (min + max)/2;
    long long int sum = 0;
    for(int i = 0; i < n; i++){
        if(treeH[i]-mid >0)
            sum += treeH[i]-mid ;
    }
    if(mid == min || sum == h)
        return mid;         
    else if(sum < h)
        BinSearch(min, mid, treeH);
    else
        BinSearch(mid, max,treeH);
}          
int main(){
    scanf("%lld %lld", &n, &h);
    long long int treeH[n];
    for(int i = 0; i < n; i++)
        scanf("%lld",&treeH[i]);  
    sort(treeH, treeH +n);    
    cout << BinSearch(0, treeH[n-1], treeH);
    return 0;
}